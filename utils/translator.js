/**
 * 翻译保险条款
 * @param {string} text - 原始条款文本
 * @returns {{ changes: Array, termCount: number, readability: { before: string, after: string } }}
 */
function translateClause(text) {
  if (!text || !text.trim()) {
    return { changes: [], termCount: 0, readability: { before: "无文本", after: "无文本" } };
  }

  const matched = [];
  const seenTerms = new Set();
  const seenExplanations = new Set();

  for (const [term, explanation] of TERM_DICT) {
    if (text.includes(term) && !seenTerms.has(term)) {
      // 检查该 term 是否已被某个更长的已匹配 term 涵盖
      let isSubsumed = false;
      for (const [matchedTerm] of matched) {
        if (matchedTerm.includes(term) && matchedTerm !== term) {
          isSubsumed = true;
          break;
        }
      }
      if (!isSubsumed && !seenExplanations.has(explanation)) {
        seenTerms.add(term);
        seenExplanations.add(explanation);
        matched.push({ term, explanation });
      }
    }
  }

  const count = matched.length;
  let before, after;
  if (count === 0) { before = "通俗"; after = "通俗"; }
  else if (count <= 2) { before = "较易懂"; after = "大白话"; }
  else if (count <= 5) { before = "有点绕"; after = "较易懂"; }
  else if (count <= 10) { before = "很难读"; after = "能看懂"; }
  else { before = "天书级别"; after = "能看懂"; }

  return {
    changes: matched,
    termCount: count,
    readability: { before, after }
  };
}

module.exports = { TERM_DICT, translateClause };
